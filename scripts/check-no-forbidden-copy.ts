#!/usr/bin/env npx tsx
/**
 * Vérifie qu'aucun terme IA interdit n'apparaît dans les surfaces utilisateur.
 * Utilisé en CI pour valider le Lot 1.
 */
import { readdirSync, readFileSync, statSync } from "fs";
import { join } from "path";

const FORBIDDEN_PATTERNS = [
  /\bIA\b/,
  /\bAI\b/,
  /intelligence artificielle/i,
  /généré par ia/i,
  /generated by ai/i,
  /\bGPT\b/,
  /\bOpenAI\b/,
  /protocoles ia/i,
  /ai protocol/i,
];

const SCAN_DIRS = ["app", "components", "messages"];

const EXCEPTION_PATHS = [
  "politique-de-confidentialite",
  "data-protection",
  "legal",
  "node_modules",
  ".next",
  "CLAUDE.md",
  "PROGRESS.md",
  "AGENTS.md",
  "scripts/check-no-forbidden-copy.ts",
];

function isException(filePath: string): boolean {
  return EXCEPTION_PATHS.some((exc) => filePath.includes(exc));
}

function scanFile(filePath: string): { file: string; line: number; text: string }[] {
  const violations: { file: string; line: number; text: string }[] = [];
  if (isException(filePath)) return violations;

  const content = readFileSync(filePath, "utf-8");
  const lines = content.split("\n");

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    // Skip code comments
    const trimmed = line.trim();
    if (trimmed.startsWith("//") || trimmed.startsWith("*") || trimmed.startsWith("/*")) {
      continue;
    }

    for (const pattern of FORBIDDEN_PATTERNS) {
      if (pattern.test(line)) {
        violations.push({ file: filePath, line: i + 1, text: line.trim() });
        break;
      }
    }
  }

  return violations;
}

function scanDir(dir: string): { file: string; line: number; text: string }[] {
  const violations: { file: string; line: number; text: string }[] = [];

  try {
    const entries = readdirSync(dir);
    for (const entry of entries) {
      const fullPath = join(dir, entry);
      if (isException(fullPath)) continue;

      const stat = statSync(fullPath);
      if (stat.isDirectory()) {
        violations.push(...scanDir(fullPath));
      } else if (/\.(tsx?|json)$/.test(entry)) {
        violations.push(...scanFile(fullPath));
      }
    }
  } catch {
    // ignore unreadable dirs
  }

  return violations;
}

const root = process.cwd();
const allViolations: { file: string; line: number; text: string }[] = [];

for (const dir of SCAN_DIRS) {
  allViolations.push(...scanDir(join(root, dir)));
}

if (allViolations.length === 0) {
  process.stdout.write(
    "✅ Aucun terme IA interdit trouvé dans les surfaces utilisateur.\n"
  );
  process.exit(0);
} else {
  console.error(`❌ ${allViolations.length} violation(s) trouvée(s) :\n`);
  for (const v of allViolations) {
    console.error(`  ${v.file}:${v.line}`);
    console.error(`    ${v.text}\n`);
  }
  process.exit(1);
}
